    private void Create_JButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Create_JButtonActionPerformed
        // TODO add your handling code here:
        try{
        Float.parseFloat(Price_JTextField.getText());
        Integer.parseInt(Seats_JTextField.getText());
        Integer.parseInt(ID_JTextField.getText());
        Airplane airplane=airplanecatalog.addAirplane();
        airplane.setPlaneName(PlaneName_JTextField.getText());
        airplane.setPrice(Float.parseFloat(Price_JTextField.getText()));
        airplane.setSeats(Integer.parseInt(Seats_JTextField.getText()));
        airplane.setPlaneID(Integer.parseInt(ID_JTextField.getText()));
        airplane.setDescription(Description_JTextField.getText());
        if(PlaneName_JTextField.getText().isEmpty()||Price_JTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please add all the Values");
            }
        else{    
            JOptionPane.showMessageDialog(null, "Airliner Created Successfully");
            }
        PlaneName_JTextField.setText("");
        Price_JTextField.setText("");
        Seats_JTextField.setText("");
        ID_JTextField.setText("");
        Description_JTextField.setText("");
            }
        catch(Exception e){
                    JOptionPane.showMessageDialog(null,"Invalid values");
        }
        
    }//GEN-LAST:event_Create_JButtonActionPerformed


